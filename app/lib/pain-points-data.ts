// Helper function to get pain point by slug
export function getPainPointBySlug(slug: string): PainPoint | undefined {
  return painPointsData.find((p) => p.slug === slug)
}

// Helper function to get all pain point slugs for static generation
export function getAllPainPointSlugs(): string[] {
  return painPointsData.map((p) => p.slug)
}

// Helper function to get seasonally relevant pain points
export function getSeasonalPainPoints(season: string): PainPoint[] {
  return painPointsData.filter(
    (p) => p.seasonalRelevance?.includes(season) || p.seasonalRelevance?.includes('all-year')
  )
}

// Helper function to get related pain points
export function getRelatedPainPoints(currentSlug: string, limit: number = 4): PainPoint[] {
  const current = getPainPointBySlug(currentSlug)
  if (!current) return painPointsData.slice(0, limit)
  
  // Find pain points with overlapping keywords or seasons
  const related = painPointsData
    .filter((p) => p.slug !== currentSlug)
    .map((p) => {
      const keywordOverlap = p.keywords.filter((k) => current.keywords.includes(k)).length
      const seasonOverlap = p.seasonalRelevance?.filter((s) => current.seasonalRelevance?.includes(s)).length || 0
      return { ...p, score: keywordOverlap + seasonOverlap }
    })
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
  
  return related
}
